            const int GRID_SIZE = 8;
            auto toNdsPx = [](float ec) { return (ec + 512.0f) / 4.0f; };
            float meshMinX=1e9f, meshMaxX=-1e9f, meshMinZ=1e9f, meshMaxZ=-1e9f;
