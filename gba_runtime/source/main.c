static void init_obj_sprites(void)
{
    int i;
    REG_DISPCNT |= DCNT_OBJ | DCNT_OBJ_1D;

#ifdef AFN_ASSET_COUNT
#if AFN_ASSET_COUNT > 0
    // Copy static OBJ tile data into VRAM — placed AFTER direction tile space
    // In Mode 4 (bitmap), tiles 0-511 overlap the framebuffer, so offset by 512.
    // In Mode 0 (tilemap), place within 1024-tile VRAM bounds using tm_static_adj.
    {
        const u32 *src = afn_all_tiles;
        // OBJ VRAM base = 0x06010000, each tile = 32 bytes
        // tile_mem[4] only indexes 512 tiles; use raw pointer for tiles >= 512
#if defined(AFN_MESH_COUNT) && AFN_MESH_COUNT > 0
        u32 *dst;
        if (afn_current_mode == 1) {
            // Mode 0: place static tiles right after compact direction slots
            dst = (u32*)(0x06010000 + tm_dir_slot_count * 32);
        } else {
            // Mode 4: skip bitmap overlap region (512 tiles) + direction tiles
            dst = (u32*)(0x06010000 + (512 + AFN_DIR_VRAM_TILES) * 32);
        }
#else
        u32 *dst = (u32*)(0x06010000 + AFN_DIR_VRAM_TILES * 32);
#endif
        for (i = 0; i < (int)(AFN_ALL_TILES_LEN / 4); i++)
            dst[i] = src[i];
    }

    // Load per-asset palettes into OBJ palette banks
    for (i = 0; i < AFN_ASSET_COUNT; i++)
    {
        int palBank = afn_asset_desc[i][4];
        int c;
        for (c = 0; c < 16; c++)
            pal_obj_mem[palBank * 16 + c] = afn_pal[i][c];
    }

    // Init direction animation set tracking — DMA set 0 from ROM at boot
#if defined(AFN_HAS_ASSET_DIRS) && AFN_ASSET_COUNT > 0
    {
        int ai;
        for (ai = 0; ai < AFN_ASSET_COUNT; ai++)
        {
            g_active_dir_set[ai] = -1; // mark as unloaded so switch_dir_anim_set works
            g_current_anim[ai] = 0;
            g_asset_anim_enabled[ai] = 0;
        }
        g_anim_frame_counter = 0;
    }
#endif

    // Load per-asset direction palettes if present
#ifdef AFN_HAS_ASSET_DIRS
    {
        int ai;
        for (ai = 0; ai < AFN_ASSET_COUNT; ai++)
        {
            if (!afn_asset_dir_desc[ai][4]) continue; // no directions
            int adPalBank = afn_asset_dir_desc[ai][3];
            int c;
            for (c = 0; c < 16; c++)
                pal_obj_mem[adPalBank * 16 + c] = afn_pal_assetdir[ai][c];
        }
    }
#endif

#endif
#else
    // No assets — use fallback solid 8x8 tile
    {
        u32 *t0 = (u32*)&tile_mem[4][0];
        int k;
        for (k = 0; k < 8; k++) t0[k] = 0x11111111;
    }
    pal_obj_mem[1*16 + 1] = RGB15(31, 31, 0);
    pal_obj_mem[2*16 + 1] = RGB15(31, 8, 8);
    pal_obj_mem[3*16 + 1] = RGB15(8, 8, 31);
    pal_obj_mem[4*16 + 1] = RGB15(8, 31, 8);
    pal_obj_mem[5*16 + 1] = RGB15(31, 16, 31);
#endif

    // Minimap dot tile
    {
#ifdef AFN_MINIMAP_TILE
        u32 *dot = (u32*)&tile_mem[4][AFN_MINIMAP_TILE];
#else
        u32 *dot = (u32*)&tile_mem[4][16];
#endif
        int k;
        for (k = 0; k < 8; k++) dot[k] = 0;
        dot[0] = 0x00000011;
        dot[1] = 0x00000011;
    }

    // Minimap palette bank (bank 6)
    pal_obj_mem[6*16 + 0] = 0;
    pal_obj_mem[6*16 + 1] = RGB15(31, 31, 31);
    pal_obj_mem[6*16 + 2] = RGB15(20, 20, 20);
    pal_obj_mem[6*16 + 3] = RGB15(31, 0, 0);

    oam_init(obj_mem, 128);
}

